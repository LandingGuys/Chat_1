package henu.blog.bean;

public class User {
	private int user_id;
	private String user_name;
	private String user_password;
	private String user_email;
	private String user_image;
	private String user_username;
	private String user_region;
	private String user_industry;
	private String user_position;
	private String user_introduction;
	
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public String getUser_image() {
		return user_image;
	}
	public void setUser_image(String user_image) {
		this.user_image = user_image;
	}
	public String getUser_username() {
		return user_username;
	}
	public void setUser_username(String user_username) {
		this.user_username = user_username;
	}
	public String getUser_region() {
		return user_region;
	}
	public void setUser_region(String user_region) {
		this.user_region = user_region;
	}
	public String getUser_industry() {
		return user_industry;
	}
	public void setUser_industry(String user_industry) {
		this.user_industry = user_industry;
	}
	public String getUser_position() {
		return user_position;
	}
	public void setUser_position(String user_position) {
		this.user_position = user_position;
	}
	public String getUser_introduction() {
		return user_introduction;
	}
	public void setUser_introduction(String user_introduction) {
		this.user_introduction = user_introduction;
	}
	
}
